    function Prepare()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->wp = new clsSQLParameters($this->ErrorBlock);
        $this->wp->AddParameter("1", "postequipment_id", ccsInteger, "", "", $this->Parameters["postequipment_id"], "", false);
